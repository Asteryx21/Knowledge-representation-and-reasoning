    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();
            //pernoyme tis times ton text box
            String titletext = title_txt.getText();
            String daytext = day_txt.getText().toString();
            String timetext = time_txt.getText().toString();
            String proftext = professor_txt.getText().toString();

       
            try {
                DocumentBuilder db = dbf.newDocumentBuilder();
                Document document = db.parse("12_schedule.xml");
                //1o append mesa sto root element gia na valoyme to Lesson
                Element root = document.getDocumentElement();
                Element lesson = document.createElement("Lesson");
                root.appendChild(lesson);
                //2o append mesa sto lesson gia na valoyme to title
                Element title = document.createElement("Title");
                title.appendChild(document.createTextNode(titletext));
                lesson.appendChild(title);

                //3o append mesa sto lesson gia na valoyme to lecture
                Element lecture = document.createElement("Lecture");
                lesson.appendChild(lecture);
                //4o append mesa sto lecture gia na valoyme to day
                Element day = document.createElement("Day");
                day.appendChild(document.createTextNode(daytext));
                lecture.appendChild(day);
                //5o append mesa sto lecture gia na valoyme to time
                Element time = document.createElement("Time");
                time.appendChild(document.createTextNode(timetext));
                lecture.appendChild(time);
                //6o append mesa sto lesson gia na valoyme to proffesor
                Element professor = document.createElement("Professor");
                professor.appendChild(document.createTextNode(proftext));
                lesson.appendChild(professor);                
                //"update" tou xml arxeiou
                TransformerFactory factory = TransformerFactory.newInstance();
                Transformer transformer = factory.newTransformer();
                DOMSource domSource = new DOMSource(document);
                StreamResult streamResult = new StreamResult(new File("12_schedule.xml"));
                transformer.transform(domSource, streamResult);

                DOMSource source = new DOMSource(document);
         
            } catch (SAXException ex) {
                Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ParserConfigurationException ex) {
                Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
            } catch (TransformerException ex) {
                Logger.getLogger(main.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        

    }//GEN-LAST:event_jButton1ActionPerformed

