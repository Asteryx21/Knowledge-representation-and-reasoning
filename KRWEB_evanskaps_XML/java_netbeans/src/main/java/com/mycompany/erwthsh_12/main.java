    private void filter (String query){
        DefaultTableModel tblMode2 = (DefaultTableModel)xmltable.getModel();
        TableRowSorter<DefaultTableModel> tr=new TableRowSorter<DefaultTableModel>(tblMode2);
        xmltable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }
    
    
